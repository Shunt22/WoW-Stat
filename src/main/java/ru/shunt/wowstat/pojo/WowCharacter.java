package ru.shunt.wowstat.pojo;

public class WowCharacter {

	private String realmSlug;
	private String name;
	private long raceId;
	private long specId;

	public String getRealmSlug() {
		return realmSlug;
	}

	public String getName() {
		return name;
	}

	public long getRaceId() {
		return raceId;
	}

	public long getSpecId() {
		return specId;
	}

}
